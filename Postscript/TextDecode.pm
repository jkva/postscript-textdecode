#=======================
sub new {
#=======================
    my $class = shift;
    my $self = {};

    return bless $self, $class;
}

#=======================
sub _unicode_mapping {
#=======================
    my $self = shift;
    unless( $self->{ uc_to_glyph } ) {
        my %uc_to_glyph      = map { sprintf("%04X", ord( $Default_Glyphs{$_} ) )      => $_ } keys %Default_Glyphs;
        my %zfdb_uc_to_glyph = map { sprintf("%04X", ord( $Zapfdingbats_Glyphs{$_} ) ) => $_ } keys %Zapfdingbats_Glyphs;

        $self->{ uc_to_glyph } = { %uc_to_glyph, %zfdb_uc_to_glyph };
    }    
    return $self->{ uc_to_glyph };
}

#=======================
sub uni_to_chars {                  # 0x0000 .. 0xD7FF, 0xE000 .. 0xFFFF
#=======================
    my $self  = shift;
    my $uni   = shift;

    $uni =~ s/^uni(.*)$/$1/;

    while( $uni =~ /([0-9ABCDEF]{4})/ ) {
        my $char = $self->glyph_to_char( $self->_unicode_mapping->{ $1 } );
        $uni =~ s/$1/$char/;
    }

    return $uni;
}

#=======================
sub u_to_char {                     # 0x0000 .. 0xD7FF, 0xE000 .. 0x10FFFF
#=======================
    my $self = shift;
    my $u    = shift;
    
    $u =~ /^u([0-9ABCDEF]{4,6})$/;
    
    return $self->glyph_to_char( $self->_unicode_mapping->{ $1 } );
}

#=======================
sub glyph_to_char {
#=======================
    my $self = shift;
    my $glyph = shift || '.notdef';
    return ( $self->font_name eq 'ZapfDingbats'
        ? $Zapfdingbats_Glyphs{ $glyph }
        : $Default_Glyphs{ $glyph }
    ) || '';
}

#=======================
sub glyph_to_dec {
#=======================
    my $self = shift;
    my $glyph = shift;
    $self->encoding( shift );

    return $self->encoding->{glyph_to_dec}{ $glyph } || 0;
}

#=======================
sub oct_to_glyph {
#=======================
    my $self = shift;
    my $char = shift;
    $self->encoding( shift );
    return '.notdef' unless $char;

    $char = oct($char);
    my $glyph = $self->encoding->{glyph_to_char}->[ $char ];
    return defined $glyph ? $glyph : '.notdef';
}

#=======================
sub oct_to_char {
#=======================
    my $self = shift;
    my $char = shift;
    $self->encoding( shift );

    return $self->glyph_to_char( $self->oct_to_glyph( $char ) );
}

#=======================
sub ps_to_text {
#=======================
    my $self = shift;
    my $postscript = shift;
    $self->encoding( shift );

    while( $postscript =~ qr{\\(\d{1,3})} ) {           # \232
        my $decoded_char = $self->oct_to_char( $1 );
        $postscript =~ s{\\$1}{$decoded_char}g;    
    }

    while( $postscript =~ qr{/(u[ABCDEF0-9]{4,6})} ) {   # /uDEAD
        my $decoded_char = $self->u_to_char( $1 );
        $postscript =~ s{\\$1}{$decoded_char}g;    
    }

    while( $postscript =~ qr{/(uni[ABCDEF0-9]{4,})} ) {     # /uniDEADBEEF
        my $decoded_chars = $self->uni_to_chars( $1 );
        $postscript =~ s{\\$1}{$decoded_chars}g;    
    }

    return $postscript;
}

#=======================
sub font_name {
#=======================
    my $self = shift;
    if( @_ ) {
        $self->{_font_name} = shift;
    }
    return $self->{_font_name} || '_default';
}

#=======================
sub encoding {
#=======================
    my $self = shift;
    if( my $encoding = shift ) {
        $self->{_encoding} = _parse_encoding( $encoding );        
    }
    return $self->{_encoding} || die( "No encoding set!" );
}

#=======================
sub _parse_encoding {
#=======================
    my $encoding = shift;
    $encoding =~ s/\n//g;    

    my $hash = md5_hex( $encoding );
    return $Stored_Encodings{ $hash } if exists $Stored_Encodings{ $hash };

    my @glyphs = map { /^\/(.*)$/ && lc $1 } split( '\s+', $encoding );
    $Stored_Encodings{ $hash }{ glyph_to_char } = \@glyphs;

    for( my $i = 0; $i < scalar @glyphs; $i++ ) {
        $Stored_Encodings{ $hash }{ glyph_to_dec }{ $glyphs[$i] } = $i;
    }

    return $Stored_Encodings{ $hash };
}

#=======================
sub _stored_encodings {
#=======================
    return \%Stored_Encodings;
}

